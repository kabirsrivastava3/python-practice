# A program that input two tuple and create a third that contain all elements of the first followed of the second.


numTuple1 = eval(input("Enter the numbers in First Tuple: "))
print ("First Tuple:=",numTuple1)
numTuple2 = eval(input("Enter the numbers in Second Tuple: "))
print ("Second Tuple:=",numTuple2)

numTuple3 = numTuple1 + numTuple2

print ("Third Tuple:=",numTuple3)

